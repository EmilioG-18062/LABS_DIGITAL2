uint8_t voltage1 = 0;


/*//////////////////////////////////////////////////////////////////////////////
 * INTERRUPCIONES
 */
void __interrupt () myISR(void){
    //ADC Interrups
    if (PIR1bits.ADIF == 1 && ADCON0bits.GO_nDONE == 0){
        voltage1 = ADRESH;
        __delay_us(40);
        ADC_FLAG_SetLow();
        GOnDONE_SetHigh();
    }
    
    //Interruocion del SPI
    if(PIR1bits.SSPIF){
        if(!SSPSTATbits.BF){
            PORTD = SSPBUF;
        }
        SSPBUF = voltage1;
        PIR1bits.SSPIF = 0;
    }
}

